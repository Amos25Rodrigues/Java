package introducao;
public class Variaveis {
    public static void main(String[] args) {
        
        /*DECLARANDO VARIÁVEIS*/
        int minhaIdade = 20;
        String meuNome = "Amós";
        char meuSexo = 'M';
        float minhaAltura = 1.68f;
        var variavel = 1; // var aceita qualquer tipo primitivo, recebendo 
        System.out.println(minhaIdade);
        System.out.println(meuNome);
        System.out.println(meuSexo);
        System.out.println(minhaAltura);
        System.out.println(variavel);

        /* TIPOS PRIMITIVOS
        * numeros inteiros = 100, 255, 219.201.108.231.903
        *
        * byte => 8 bits => -128 a 127 = 255
        * short => 16 bits => -32.768 a 32.767
        * int => 32 bits => -2.147.483.648 a 2.147.483.647
        * long => 64 bits => -9.223.372.036.854.775.808 a 9.223.372.036. * 854.775.807
        * 
        * numeros decimais = 1.2, 3.4, 5.6, 7.8, 9.0 ou 5.50
        * 
        * float => 32 bits => precisão simples
        * double => 64 bits => precisão dupla
        *  
        * String => representa palavras e frases => "abcd1234!@#$"
        * char => representa um unico caractere => 'a', 'b', '1', '2', '!', '@'
        * 
        * boolean => true ou false (1,0) 
        */
    }
}
